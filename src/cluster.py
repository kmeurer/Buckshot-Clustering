class cluster:
	# Constructor: takes an entry as the centroid and an array of entries as the second argument
	def __init__(self, centroid, points):
		self.centroid = centroid
		self.points = points

	# get the current centroid
	def getCentroid():
		return self.centroid

	# get the points
	def getPoints():
		return self.points


